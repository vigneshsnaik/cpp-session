#include <iostream>
using namespace std;
int main() {
    int a = 10, b = 20;

    cout << "sum: " << endl;       // << a + b << endl;
    cout << "diff: " << endl;      // << a - b << endl;
    cout << "product: " << endl;   // << a * b << endl;
    cout << "quotient: " << endl;  // << a / b << endl;
    cout << "remainder: " << endl; // << a % b << endl;

    return 0;
}