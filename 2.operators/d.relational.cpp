#include <iostream>
using namespace std;
int main() {
    int a = 10, b = 20;
    cout << "a < b: " << endl;  // << a < b << endl;
    cout << "a > b: " << endl;  // << a > b << endl;
    cout << "a <= b: " << endl; // << a <= b << endl;
    cout << "a >= b: " << endl; // << a >= b << endl;
    cout << "a == b: " << endl; // << a == b << endl;
    cout << "a != b: " << endl; // << a != b << endl;
    return 0;
}