#include <iostream>
#include <vector>

int main() {
    // Create a vector of integers
    std::vector<int> myVector = {1, 2, 3, 4, 5};

    // Get the size of the vector
    int size = myVector.size();

    // Print the size of the vector
    std::cout << "The size of the vector is: " << size << std::endl;

    return 0;
}
